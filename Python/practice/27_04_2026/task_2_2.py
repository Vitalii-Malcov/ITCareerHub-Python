""" 02 Обратный список

Напишите программу, которая
- принимает строку с числами через пробел;
- трансформирует строку в список целых чисел;
- и выводит его в обратном порядке.

ОБРАТИТЕ ВНИМЕНИЕ!
В списке должны быть именно числа, а не строки!

Пример вывода:
Список чисел через пробел: 1 2 3 4 5
Обратный список: [5, 4, 3, 2, 1]
"""

numbers = "1 2 3 4 5"  # Строка с числами, разделёнными пробелами

num = numbers.split()  # Разбиваем строку по пробелам и получаем список строк
for i in range(len(num)):  # Цикл по индексам списка
    num[i] = int(num[i])  # Преобразуем каждый элемент из строки в целое число

print("Список чисел через пробел:", num[::-1])  # Выводим список в обратном порядке (срез [::-1])
