""" 04 Максимальное число

Напишите программу, которая
- выводит наибольшее число в списке (не используя встроенную функцию max()!!!),
- а также его индекс в списке.

numbers = [3, 12, 8, 22, 9, 25, 6, 23, 8, 7]
Пример вывода:
Список чисел: [3, 12, 8, 22, 9, 25, 6, 23, 8, 7]
Наибольшее число: 25
Индекс числа: 5
"""

numbers = [3, 12, 8, 22, 9, 25, 6, 23, 8, 7]  # Список чисел для поиска максимума
print("Список чисел:", numbers)  # Выводим исходный список

max_number = numbers[0]  # Устанавливаем первый элемент как начальное максимальное значение
max_index = 0  # Индекс максимального числа (начинаем с 0)

for i in range(1, len(numbers)):  # Цикл от индекса 1 до конца списка
    if numbers[i] > max_number:  # Если текущее число больше максимума
        max_number = numbers[i]  # Обновляем максимум
        max_index = i  # Запоминаем индекс этого числа
print(max_number)  # Выводим максимальное число (25)
print(max_index)  # Выводим его индекс (5)
