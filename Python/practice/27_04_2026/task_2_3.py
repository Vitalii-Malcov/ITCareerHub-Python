""" 03 Сумма чётных и нечётных чисел

Напишите программу, которая
- подсчитывает сумму чётных и нечётных чисел в списке по отдельности.

numbers = [-1, 8, 2, 0, -3, -9, -1, 10, 7, -4]

Пример вывода:
Изначальный список: [-1, 8, 2, 0, -3, -9, -1, 10, 7, -4]
Сумма чётных: 16
Сумма нечётных: -7
"""

numbers = [-1, 8, 2, 0, -3, -9, -1, 10, 7, -4]  # Список чисел для анализа
sum_even = 0  # Переменная для суммы чётных чисел
sum_odd = 0  # Переменная для суммы нечётных чисел

for num in numbers:  # Перебираем каждое число в списке
    if num % 2 == 0:  # Если число делится на 2 нацело (чётное)
        sum_even += num  # Добавляем к сумме чётных
    else:  # Иначе число нечётное
        sum_odd += num  # Добавляем к сумме нечётных
print(sum_even)  # Выводим сумму чётных (16)
print(sum_odd)  # Выводим сумму нечётных (-7)
print(sum_even - sum_odd)  # Выводим разность (16 - (-7) = 23)
