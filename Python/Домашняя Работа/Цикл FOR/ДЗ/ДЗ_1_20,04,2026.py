# Таблица умножения
#
# Напишите программу, которая выводит таблицу умножения.
# Для чисел от 1 до n. Где n это число, которое введет пользователь.
# Оформите вывод так, чтобы столбцы были ровные (число ровно под числом)
#
# Пример вывода:
#
# Введите число: 5


n = int(input("Введите число: "))  # Получаем число n от пользователя

for i in range(1, n + 1):  # Внешний цикл для строк (i от 1 до n)
    for j in range(1, n + 1):  # Внутренний цикл для столбцов (j от 1 до n)
        number = i * j  # Вычисляем произведение i * j

        if number < 10:  # Если число однозначное
            print(number, end="    ")  # Печатаем с 4 пробелами
        else:  # Если число двузначное
            print(number, end="   ")  # Печатаем с 3 пробелами

    print()  # Переход на новую строку после каждой строки таблицы
