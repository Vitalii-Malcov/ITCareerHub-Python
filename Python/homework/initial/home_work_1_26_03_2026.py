
# вводим состояние света (1 — включен, 0 — выключен)
light = int(input("Свет (1 - вкл, 0 - выкл): "))
# вводим состояние окна (1 — открыто, 0 — закрыто)
window = int(input("Окно (1 - открыто, 0 - закрыто): "))

# проверяем, выполнены ли оба условия (и свет, и окно)
both = light and window

# проверяем, выполнено ли хотя бы одно условие
one = light or window

# выводим результат (преобразуем в True/False)
print("Оба условия выполнены:", both)
print("Хотя бы одно условие выполнено:", one)
