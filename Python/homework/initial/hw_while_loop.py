# # Напишите программу, которая генерирует случайное число
# # от 1 до 100 включительно и дает пользователю 10 попыток,
# # чтобы угадать это число. Программа должна подсказывать,
# # больше или меньше загаданное число.
# # После завершения игры программа оценивает,
# # насколько хорошая интуиция у игрока.
# ## Пример вывода:
# ## Угадайте число от 1 до 100. У вас 10 попыток.
# # Ваше предположение: 50
# # Загаданное число меньше вашего
# # Ваше предположение: 25
# # Загаданное число больше вашего
# # Ваше предположение: 30
# # Поздравляем! Вы угадали число: 30.
# ## Вы угадали число за 3 попытки. Отличный результат!
# ## Угадайте число от 1 до 100. У вас 10 попыток.
# # Ваше предположение: 2
# # Поздравляем! Вы угадали число: 2 за 1 попыток. Отличный результат!
# # Отличная интуиция! Вы угадали с первой попытки!
#
#
import random  # Подключаем модуль random для генерации случайных чисел

# Генерируем случайное число от 1 до 100 включительно
secret_number = random.randint(1, 100)

max_attempts = 10  # Максимальное количество попыток
attempts_used = 0  # Счётчик использованных попыток

print("Угадайте число от 1 до 100. У вас 10 попыток.")

# Цикл while — повторяется, пока попыток меньше максимума
while attempts_used < max_attempts:

    # Читаем ввод пользователя и сразу преобразуем в число
    try:
        guess = int(input("Ваше предположение: "))
        if guess < 1 or guess > 100:
            print("Пожалуйста, введите число от 1 до 100.")
            continue
    except ValueError:
        print("Пожалуйста, введите целое число.")
        continue
# try - блок: пытаемся прочитать ввод пользователя как строку, преобразовать в int и сохранить в guess.
# Если guess не в диапазоне 1-100, выводим сообщение и continue (переходим к следующей итерации цикла, не считая попытку).
# except ValueError: если ввод не число (например, буквы), ловим ошибку, выводим сообщение и continue.

    attempts_used += 1  # Увеличиваем счётчик на 1 после каждого угадывания

    print("У вас осталось", max_attempts - attempts_used, "попыток.")

    if guess == secret_number:  # Если угадал точно
        print("Поздравляем! Вы угадали число:", secret_number, "за", attempts_used, "попыток.")

        # Оцениваем результат в зависимости от числа попыток
        if attempts_used == 1:
            print("Отличный результат!")
            print("Отличная интуиция! Вы угадали с первой попытки!")
        elif attempts_used <= 3:
            print("Отличный результат!")
        elif attempts_used <= 6:
            print("Хороший результат!")
        else:
            print("Неплохо, но можно лучше!")

        break  # Выходим из цикла — игра окончена

    elif guess < secret_number:  # Если ввёл меньше загаданного
        print("Загаданное число больше вашего")
    else:  # Если ввёл больше загаданного
        print("Загаданное число меньше вашего")

# Этот блок выполняется только если цикл завершился БЕЗ break (попытки кончились)
else:
    print("Попытки закончились! Загаданное число было: ", secret_number)
    print("Не расстраивайтесь, попробуйте ещё раз!")
