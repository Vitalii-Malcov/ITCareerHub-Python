price = 127  # Устанавливаем стоимость товара

coins = [50, 10, 5, 2, 1]  # Список номиналов монет в порядке убывания

for coin in coins:  # Цикл по каждому номиналу монеты
    count = price // coin  # Вычисляем количество монет этого номинала (целая часть деления)
    if count > 0:  # Если нужна хотя бы одна монета этого номинала
        print("Внесите монеты номиналом", coin, ":", count)  # Выводим результат
        price = price % coin  # Обновляем оставшуюся сумму (остаток от деления)


