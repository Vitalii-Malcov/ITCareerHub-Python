
# Напишите программу, которая принимает строку
# и удаляет из неё все повторяющиеся символы,
# сохраняя только первые их вхождения.
#
# Пример вывода:
#
# Введите строку: Python programming
#
# Результат: Python prgami



text = input("Введите строку: ")  # Получаем строку от пользователя

result = ""  # Инициализируем пустую строку для результата

for char in text:  # Проходим по каждому символу в строке
    if char not in result:  # Проверяем, нет ли символа в результирующей строке
        result = result + char  # Если символа нет, добавляем его в результат

print("Результат:", result)  # Выводим результат без повторяющихся символов
